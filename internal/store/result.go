package store

import (
	"sync"
	"time"
)

// ResultStore abstracts result storage for testability.
type ResultStore interface {
	Put(r *Result)
	Get(url string) (*Result, bool)
	All() []*Result
	Count() int
}

type memStore struct {
	mu      sync.RWMutex
	results map[string]*Result
}

func NewStore() *memStore {
	return &memStore{
		results: make(map[string]*Result),
	}
}

func (s *memStore) Put(r *Result) {
	s.mu.Lock()
	defer s.mu.Unlock()
	s.results[r.URL] = r
}

func (s *memStore) Get(url string) (*Result, bool) {
	s.mu.RLock()
	defer s.mu.RUnlock()
	r, ok := s.results[url]
	return r, ok
}

func (s *memStore) All() []*Result {
	s.mu.RLock()
	defer s.mu.RUnlock()
	results := make([]*Result, 0, len(s.results))
	for _, r := range s.results {
		results = append(results, r)
	}
	return results
}

func (s *memStore) Count() int {
	s.mu.RLock()
	defer s.mu.RUnlock()
	return len(s.results)
}

// Result is the universal output type for any processed URL.
type Result struct {
	URL         string       `json:"url"`
	Title       string       `json:"title,omitempty"`
	Description string       `json:"description,omitempty"`
	Text        string       `json:"text,omitempty"`
	Links       []string     `json:"links,omitempty"`
	FilePath    string       `json:"file_path,omitempty"` // for binary downloads
	YouTube     *YouTubeData `json:"youtube,omitempty"`
	Error       string       `json:"error,omitempty"`
	FetchedAt   time.Time    `json:"fetched_at"`
	Model       string       `json:"model,omitempty"`
	NumCtx      int          `json:"num_ctx,omitempty"`
	Styles      []string     `json:"styles,omitempty"`
}

// YouTubeData holds YouTube-specific extracted data, embedded in Result.
type YouTubeData struct {
	VideoID       string             `json:"video_id,omitempty"`
	Duration      string             `json:"duration,omitempty"`
	ViewCount     int                `json:"view_count,omitempty"`
	UploadDate    string             `json:"upload_date,omitempty"`
	ChannelName   string             `json:"channel_name,omitempty"`
	ChannelID     string             `json:"channel_id,omitempty"`
	Tags          []string           `json:"tags,omitempty"`
	Captions      []CaptionTrack     `json:"captions,omitempty"`
	AudioPath     string             `json:"audio_path,omitempty"`
	SubtitlePaths map[string]string  `json:"subtitle_paths,omitempty"`
	PlaylistItems []PlaylistItem     `json:"playlist_items,omitempty"`
	Processing    []ProcessingResult `json:"processing,omitempty"`
}

// ProcessingResult holds the output of a single LLM processing style.
type ProcessingResult struct {
	Style   string   `json:"style"`
	Content []string `json:"content"`
}

// CaptionTrack represents a single caption/subtitle track.
type CaptionTrack struct {
	Language      string `json:"language"`
	LanguageCode  string `json:"language_code"`
	AutoGenerated bool   `json:"auto_generated"`
	Text          string `json:"text"`
}

// PlaylistItem represents a single video in a playlist.
type PlaylistItem struct {
	VideoID string `json:"video_id"`
	Title   string `json:"title"`
	Index   int    `json:"index"`
}
